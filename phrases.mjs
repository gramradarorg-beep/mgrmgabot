// Пороги тиров по объёму сделки в TON.
export const TIERS = [
  { name: "small", max: 5 },
  { name: "medium", max: 25 },
  { name: "large", max: 100 },
  { name: "whale", max: Infinity }, // для продаж тот же порог зовётся "dump"
];

export function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function tierFor(ton) {
  return TIERS.find((t) => ton < t.max)?.name ?? "small";
}
